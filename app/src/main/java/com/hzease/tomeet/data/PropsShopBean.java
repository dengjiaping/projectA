public class PropsShopBean {

    private String propsName;
    private int propsIcon;
    private String propsDic;
    private String propsMoney;

    public String getPropsName() {
        return propsName;
    }

    public void setPropsName(String propsName) {
        this.propsName = propsName;
    }

    public int getPropsIcon() {
        return propsIcon;
    }

    public void setPropsIcon(int propsIcon) {
        this.propsIcon = propsIcon;
    }

    public String getPropsDic() {
        return propsDic;
    }

    public void setPropsDic(String propsDic) {
        this.propsDic = propsDic;
    }

    public String getPropsMoney() {
        return propsMoney;
    }

    public void setPropsMoney(String propsMoney) {
        this.propsMoney = propsMoney;
    }
}
